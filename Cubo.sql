SELECT
	* 
FROM
	F_VENDA
INNER JOIN
	D_PRODUTO 
ON
	D_PRODUTO.IdProduto = F_VENDA.IdProduto 
INNER JOIN
	D_LOJA 
ON
	D_LOJA.IdLoja = F_VENDA.IdLoja 
INNER JOIN
	D_DATA 
ON
	D_DATA.IdData = F_VENDA.IdData
WHERE
	D_LOJA.Nome_loja = 'Central'
GO